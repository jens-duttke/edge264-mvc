parts = [HEADERS]
# AU0: anchor IDR, both views reference, poc 0
parts.append(base_idr(0, 0))
parts.append(dep_idr(0, 0))
# AU1..N-2 have increasing high POCs (4,6,8,...) so the decoder must hold them
# in the reorder buffer; the LAST AU is the shift-AU with the lowest non-zero
# POC (2), decoded last under full-DPB pressure, so it is the frame force-bumped
# while its (gap-shifted) dependent is still unbumped in to_get_frames.
SHIFT = N - 1            # the shift access unit (decoded last, lowest POC)
for i in range(1, N):
    poc = 2 if i == SHIFT else 2 * (i + 1)
    parts.append(base_p(i, poc, ref_idc=2))
    # the LAGN dependents right before the shift AU are non-reference, so the
    # dependent view's PrevRefFrameNum stays behind by LAGN; the shift AU's
    # reference dependent then carries a frame_num gap of LAGN+1 -> LAGN inferred
    # gap-fill frames between the base and its dependent (as on the real stream).
    dep_idc = 0 if (SHIFT - LAGN) <= i < SHIFT else 2
    parts.append(dep_b(i, poc, ref_idc=dep_idc))

with open(out, "w") as f:
    f.write("".join(parts))
print(f"wrote {out}: {N} AUs, {LAGN} non-reference dependents before the shift AU")
