# Generate a multi-AU Stereo-High MVC YAML for tests/gen_avc.py that creates
# DPB pressure (many small 1x1 frames buffered under a paced consumer) and a
# dependent-view frame_num gap, so the AU's dependent lands at base+2 (a
# non-existing gap-fill frame between them) - the structure that exposes the
# old FrameId+1 view-pairing shortcut.
